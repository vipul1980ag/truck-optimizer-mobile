  /* ── Detect / persist language ── */
  function detectLang() {
    var stored = localStorage.getItem('toc_lang');
    if (stored && T[stored]) return stored;
    var nav = (navigator.language || navigator.userLanguage || 'en').substring(0, 2).toLowerCase();
    return T[nav] ? nav : 'en';
  }

  var currentLang = detectLang();

  window.t = function (key) {
    var dict = T[currentLang] || T['en'];
    return dict[key] !== undefined ? dict[key] : (T['en'][key] || key);
  };

  window.setPortalLang = function (code) {
    if (!T[code]) return;
    currentLang = code;
    localStorage.setItem('toc_lang', code);
    applyI18n();
    updateLangPicker();
  };

  /* ── Apply translations to [data-i18n] elements ── */
  function applyI18n() {
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      var attr = el.getAttribute('data-i18n-attr');
      var val = window.t(key);
      if (attr) {
        el.setAttribute(attr, val);
      } else {
        el.textContent = val;
      }
    });
  }

  /* ── Inject language picker into .nav-right ── */
  function injectLangPicker() {
    var navRight = document.querySelector('.nav-right');
    if (!navRight) return;

    var wrap = document.createElement('div');
    wrap.id = 'i18n-picker';
    wrap.style.cssText = 'display:flex;align-items:center;gap:4px;margin-right:4px;';

    LANGS.forEach(function (l) {
      var btn = document.createElement('button');
      btn.className = 'i18n-lang-btn';
      btn.setAttribute('data-lang', l.code);
      btn.title = l.name;
      btn.onclick = function () { window.setPortalLang(l.code); };
      btn.innerHTML = '<span class="i18n-flag">' + l.flag + '</span><span class="i18n-code">' + l.code.toUpperCase() + '</span>';
      wrap.appendChild(btn);
    });

    /* Insert before the first child of nav-right */
    navRight.insertBefore(wrap, navRight.firstChild);

    /* Inject styles */
    var style = document.createElement('style');
    style.textContent = [
      '.i18n-lang-btn{display:flex;align-items:center;gap:3px;padding:4px 7px;border-radius:14px;border:1px solid rgba(255,255,255,0.12);background:rgba(255,255,255,0.07);cursor:pointer;transition:background .15s,border-color .15s;}',
      '.i18n-lang-btn:hover{background:rgba(96,165,250,0.18);border-color:rgba(96,165,250,0.4);}',
      '.i18n-lang-btn.active{background:rgba(37,99,235,0.4);border-color:rgba(96,165,250,0.6);}',
      '.i18n-flag{font-size:13px;line-height:1;}',
      '.i18n-code{font-size:9px;font-weight:700;color:#94a3b8;letter-spacing:.4px;}',
      '.i18n-lang-btn.active .i18n-code{color:#93c5fd;}',
      '@media(max-width:640px){.i18n-code{display:none;}.i18n-lang-btn{padding:4px 5px;}}',
    ].join('');
    document.head.appendChild(style);

    updateLangPicker();
  }

  function updateLangPicker() {
    document.querySelectorAll('.i18n-lang-btn').forEach(function (btn) {
      btn.classList.toggle('active', btn.getAttribute('data-lang') === currentLang);
    });
  }

  /* ── Boot ── */
  document.addEventListener('DOMContentLoaded', function () {
    injectLangPicker();
    applyI18n();
  });

